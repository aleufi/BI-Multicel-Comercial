SELECT  
    D2_CLIENTE,
    A1_NOME, A1_NREDUZ, A1_VEND,
    A3_NOME,
    CONVERT(DATE, D2_EMISSAO, 112) AS D2_EMISSAO,
    D2_COD, D2_CF, D2_DOC,
    D2_ITEM, B1_DESC, B1_MCCATEG,
    CASE
        WHEN B1_MCCATEG = '1' THEN 'PIGMENTOS'
        WHEN B1_MCCATEG = '2' THEN 'OXIDOS'
        WHEN B1_MCCATEG = '3' THEN 'POS'
        WHEN B1_MCCATEG = '4' THEN 'DISPERSAO'
        WHEN B1_MCCATEG = '5' THEN 'REVENDA'
        ELSE 'OUTROS'
    END AS CATEG,
    B1_GRUPO, B1_DESCGRP,
    D2_QUANT, D2_QTDEDEV, D2_PRCVEN, D2_TOTAL, D2_VALDEV,
    D2_TOTAL - (
        ISNULL(D2_VALIPI,0) + ISNULL(D2_VALICM,0) + ISNULL(D2_ICMSRET,0) + 
        ISNULL(D2_ITEMORI,0) + ISNULL(D2_VALFRE,0) + ISNULL(D2_VALINS,0) + 
        ISNULL(D2_VALISS,0) + ISNULL(D2_ICMFRET,0) + ISNULL(D2_VALPIS,0) + 
        ISNULL(D2_VALCOF,0) + ISNULL(D2_VALCSL,0) + ISNULL(D2_VALIRRF,0) + 
        ISNULL(D2_VALIMP5,0) + ISNULL(D2_VALIMP6,0)
    ) AS D2_VRLIQ,
    CONVERT(DATE, D1_EMISSAO, 112) AS D1_EMISSAO,
    D1_QUANT, D1_TOTAL,
    D1_TOTAL - (
        ISNULL(D1_VALIPI,0) + ISNULL(D1_VALICM,0) + ISNULL(D1_ICMSRET,0) + 
        ISNULL(D1_VALFRE,0) + ISNULL(D1_VALINS,0) + ISNULL(D1_VALISS,0) + 
        ISNULL(D1_VALPIS,0) + ISNULL(D1_VALCOF,0) + ISNULL(D1_VALCSL,0) + 
        ISNULL(D1_VALIMP5,0) + ISNULL(D1_VALIMP6,0)
    ) AS D1_VRLIQ
FROM SD2010
JOIN SB1010 ON B1_FILIAL = D2_FILIAL AND B1_COD = D2_COD
JOIN SA1010 ON A1_FILIAL = D2_FILIAL AND A1_COD = D2_CLIENTE
JOIN SA3010 ON A3_COD = A1_VEND
LEFT JOIN SD1010 ON D2_DOC = D1_NFORI AND D2_COD = D1_COD AND D2_ITEM = D1_ITEMORI
WHERE
    D2_EMISSAO >= CONVERT(VARCHAR, YEAR(GETDATE()) - 5) + '0101'
    AND D2_CF IN ('5101','5102','5122','6101','6102','6109','6122','7101','7102')
    AND SD2010.D_E_L_E_T_ = ''
    AND SB1010.D_E_L_E_T_ = ''
    AND SA1010.D_E_L_E_T_ = ''
    AND SA3010.D_E_L_E_T_ = ''
    AND (SD1010.D_E_L_E_T_ = '' OR SD1010.D_E_L_E_T_ IS NULL)
    AND B1_MCCATEG IN ('1','2','3','4','5')
    AND A1_FILIAL <> '02'



