SELECT  
    D2_CLIENTE,
    A1_NOME, A1_NREDUZ, A1_VEND,
    A3_NOME,
    CONVERT(DATE, D2_EMISSAO, 112) AS D2_EMISSAO,
    D2_COD, D2_CF, D2_DOC,
    D2_ITEM, 
	B1_COD, B1_DESC, B1_MCCATEG,

    CASE
        WHEN B1_MCCATEG = 1 THEN 'PIGMENTOS' 
       WHEN B1_MCCATEG = 2 THEN 'OXIDOS' 
        WHEN B1_MCCATEG = 3 THEN 'POS'  
        WHEN B1_MCCATEG = 4 THEN 'DISPERSAO'  
        WHEN B1_MCCATEG = 5 THEN 'REVENDA'
    END AS CATEG,

    B1_GRUPO, B1_DESCGRP,
    D2_QUANT, D2_QTDEDEV, D2_PRCVEN, D2_TOTAL, D2_VALDEV,

    D2_TOTAL - (
        D2_VALIPI + D2_VALICM + D2_ICMSRET + D2_ITEMORI + D2_VALFRE + D2_VALINS + D2_VALISS + D2_ICMFRET + 
        D2_VALPIS + D2_VALCOF + D2_VALCSL + D2_VALIRRF + D2_VALIMP5 + D2_VALIMP6
    ) AS D2_VRLIQ
	
	,

    D1_EMISSAO, D1_QUANT, D1_TOTAL,

    D1_TOTAL - (
        D1_VALIPI + D1_VALICM + D1_ICMSRET + D1_VALFRE + D1_VALINS + D1_VALISS + D1_VALPIS + D1_VALCOF + 
        D1_VALCSL + D1_VALIMP5 + D1_VALIMP6
    ) AS D1_VRLIQ,

    C6_PRCVEN, C5_ACRSFIN, (C6_PRCVEN * (C5_ACRSFIN / 100 + 1)) AS PRCVENFINAL,
	C5_MOEDA, C5_CONDPAG, E4_DESCRI

FROM SD2010

JOIN SB1010 ON B1_FILIAL = D2_FILIAL AND B1_COD = D2_COD
JOIN SA1010 ON A1_FILIAL = D2_FILIAL AND A1_COD = D2_CLIENTE
JOIN SA3010 ON A3_COD = A1_VEND
JOIN SM2010 ON M2_DATA = D2_EMISSAO
JOIN SC6010 ON C6_FILIAL = D2_FILIAL AND C6_NOTA = D2_DOC AND C6_PRODUTO = B1_COD AND D2_ITEMPV = C6_ITEM
JOIN SC5010 ON C5_FILIAL = C6_FILIAL AND C5_NUM = C6_NUM
JOIN SE4010 ON E4_CODIGO = C5_CONDPAG AND E4_FILIAL = C5_FILIAL


LEFT JOIN SD1010 ON D2_DOC = D1_NFORI AND D2_COD = D1_COD AND D2_ITEM = D1_ITEMORI

