
SELECT MAX(B1_COD) AS B1_COD, MAX(B1_DESC) AS B1_DESC, MAX(D2_LOTECTL) AS D2_LOTECTL, MAX(B8_QTDORI) AS B8_QTDORI, MAX(B8_SALDO) AS B8_SALDO,
	   MAX(D2_EMISSAO) AS D2_EMISSAO, MAX(B8_DATA) AS B8_DATA, MAX(B8_SDMOV) AS B8_SDMOV, MAX(D2_QUANT) AS D2_QUANT,

	   DATEDIFF(DAY, 
        MAX(CONVERT(DATE, B8_DATA, 112)), 
        MAX(CONVERT(DATE, D2_EMISSAO, 112))
    ) AS DIFDIAS

FROM SD2010

JOIN SB8010 ON 
	B8_LOTECTL = D2_LOTECTL
	AND B8_FILIAL = D2_FILIAL
	AND D2_LOCAL = B8_LOCAL

JOIN SB1010 ON 
		B1_COD = D2_COD
	AND B1_FILIAL = D2_FILIAL
	

WHERE
	
	D2_CF IN ('5101','5102','5122','6101','6102','6109','6122','7101','7102')

	AND SD2010.D_E_L_E_T_ = ''
	AND SB8010.D_E_L_E_T_ = ''
	AND SB1010.D_E_L_E_T_ = ''

	--AND B8_LOCAL = '06'

	AND LEFT(SD2010.D2_EMISSAO, 6) = FORMAT(GETDATE(), 'yyyyMM')

GROUP BY D2_LOTECTL

ORDER BY D2_LOTECTL



--------------------------------------------------------------------------------------


SELECT 

    D2.D2_LOTECTL, 
    MAX(D2.D2_EMISSAO) AS D2_EMISSAO

FROM 
    SD2010 D2

INNER JOIN 
    SB8010 B8 ON D2.D2_LOTECTL = B8.B8_LOTECTL 
              AND D2.D2_FILIAL = B8.B8_FILIAL 
              AND D2.D2_COD = B8.B8_PRODUTO
WHERE

    D2.D2_CF IN ('5101','5102','5122','6101','6102','6109','6122','7101','7102')
    AND D2.D_E_L_E_T_ = ''
    AND B8.D_E_L_E_T_ = ''

    AND DATEDIFF(DAY, 
        CONVERT(DATE, B8.B8_DATA, 112), 
        CONVERT(DATE, D2.D2_EMISSAO, 112)
    ) > 90

	AND B8_LOCAL = '06'

GROUP BY 

    D2.D2_LOTECTL

ORDER BY 

    D2_EMISSAO DESC;


