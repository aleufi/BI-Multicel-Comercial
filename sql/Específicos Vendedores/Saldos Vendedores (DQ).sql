SELECT B2_FILIAL, B2_COD, 

	CASE
		WHEN B1_TIPO = 'MP' THEN 'MP MULTICEL'
		WHEN B1_TIPO = 'PA' THEN B1_DESC
	END AS DESCRI,

B1_CI, B1_TIPO, B1_GRUPO, B1_DESCGRP, B2_LOCAL, (B2_QATU - B2_RESERVA - B2_QEMP) AS QDISP

FROM SB2010, SB1010 

WHERE

	B2_QATU > 0 
AND B1_COD   = B2_COD  
AND B1_FILIAL = B2_FILIAL 
AND (B2_LOCAL = '01' OR B2_LOCAL = '06') 
AND B1_GRUPO IN ('1000','1001','1100','1101','1200','1201','1300','1301','1500','1501','1600','4100','4101') 

AND SB2010.D_E_L_E_T_ = '' 
AND SB1010.D_E_L_E_T_='' 
