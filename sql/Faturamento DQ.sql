		A3_NOME,
		D2_EMISSAO, D2_COD, D2_CF, D2_DOC, D2_ITEM,
		B1_DESC, B1_MCCATEG,
			CASE
				WHEN B1_MCCATEG = 1 THEN 'PIGMENTOS' 
                WHEN B1_MCCATEG = 2 THEN 'OXIDOS' 
                WHEN B1_MCCATEG = 3 THEN 'POS'  
                WHEN B1_MCCATEG = 4 THEN 'DISPERSAO'  
                WHEN B1_MCCATEG = 5 THEN 'REVENDA'
			END
			AS CATEG,
		B1_GRUPO, B1_DESCGRP,
		D2_QUANT, D2_QTDEDEV, D2_PRCVEN, B1_VUNCP, B1_VUNGGF, B1_VUNOH, D2_TOTAL, D2_VALDEV, D2_VALIPI, D2_VALICM, D2_ICMSRET, D2_ITEMORI, D2_VALFRE, D2_VALINS, D2_VALISS, D2_ICMFRET, D2_VALPIS, D2_VALCOF, D2_VALCSL, D2_VALIRRF, D2_VALIMP5, D2_VALIMP6,

		D2_TOTAL - (D2_VALIPI + D2_VALICM + D2_ICMSRET + D2_ITEMORI + D2_VALFRE + D2_VALINS + D2_VALISS + D2_ICMFRET + D2_VALPIS + D2_VALCOF + D2_VALCSL + D2_VALIRRF + D2_VALIMP5 + D2_VALIMP6) AS D2_VRLIQ,

		B1_VUNCP * D2_QUANT AS D2_CUSTMP,
		B1_VUNGGF * D2_QUANT AS D2_CUSTGGF,
		B1_VUNOH * D2_QUANT AS D2_CUSTOH,
		D2_PRCVEN - B1_VUNCP AS D2_MC,
		D2_PRCVEN - (B1_VUNCP + B1_VUNGGF) AS D2_MB,
		D2_PRCVEN - (B1_VUNCP + B1_VUNGGF + B1_VUNOH) AS D2_ML

FROM SD2010, SB1010, SA1010, SA3010

WHERE

	SD2010.D2_EMISSAO = FORMAT(GETDATE(), 'yyyyMMdd')
	AND D2_CF IN ('5101','5102','5122','6101','6102','6109','6122','7101','7102')
	AND B1_FILIAL = D2_FILIAL
	AND B1_COD = D2_COD
	AND A1_FILIAL = D2_FILIAL
	AND A1_COD = D2_CLIENTE
	AND A3_COD = A1_VEND
	AND SA3010.D_E_L_E_T_ = ''
	AND SD2010.D_E_L_E_T_ = ''
	AND SB1010.D_E_L_E_T_ = ''

