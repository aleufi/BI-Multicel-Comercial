-- Devoluçőes Dia com nome do Cliente

SELECT 
    D1.D1_FILIAL,
    D1.D1_EMISSAO,
	D2.D2_EMISSAO,
    D1.D1_COD,
    A1.A1_NOME,
    D1.D1_FORNECE,
    D1.D1_DOC,
    D1.D1_NFORI,
    B1.B1_MCCATEG,
    B1.B1_GRUPO,
    B1.B1_DESCGRP,
    D1.D1_QUANT,
    D1.D1_CF,
    D1.D1_TOTAL,
    D1.D1_VALIPI,
    D1.D1_VALICM,
    D1.D1_ICMSRET,
    D1.D1_VALFRE,
    D1.D1_VALINS,
    D1.D1_VALISS,
    D1.D1_VALPIS,
    D1.D1_VALCOF,
    D1.D1_VALCSL,
    D1.D1_VALIMP5,
    D1.D1_VALIMP6,

    D1.D1_TOTAL - (
        D1.D1_VALIPI + D1.D1_VALICM + D1.D1_ICMSRET + D1.D1_VALFRE + D1.D1_VALINS + 
        D1.D1_VALISS + D1.D1_VALPIS + D1.D1_VALCOF + D1.D1_VALCSL + 
        D1.D1_VALIMP5 + D1.D1_VALIMP6
    ) AS D1_VRLIQ

FROM SD1010 D1

INNER JOIN SB1010 B1 ON 
    B1.B1_FILIAL = D1.D1_FILIAL AND 
    B1.B1_COD = D1.D1_COD AND 
    B1.D_E_L_E_T_ = ''

INNER JOIN SA1010 A1 ON 
    A1.A1_COD = D1.D1_FORNECE AND 
    A1.A1_FILIAL = D1.D1_FILIAL AND 
    A1.D_E_L_E_T_ = ''

LEFT JOIN SD2010 D2 ON 
	D1.D1_NFORI = D2.D2_DOC AND
	D1.D1_FILIAL = D2.D2_FILIAL AND
	D1.D1_COD = D2.D2_COD AND
	D1.D1_ITEMORI = D2_ITEM

WHERE 
    LEFT(D1.D1_EMISSAO, 6) = FORMAT(GETDATE(), 'yyyyMMdd')
    AND D1.D1_FILIAL IN ('01', '03')
    AND D1.D_E_L_E_T_ = ''
    AND D1.D1_CF IN (
        '1201', '1202', '1203', '1204',
        '2201', '2202', '2203', '2204',
        '3201', '3202', '3203', '3204'
	)
	AND D2.D2_EMISSAO = FORMAT(GETDATE(), 'yyyyMMdd')


