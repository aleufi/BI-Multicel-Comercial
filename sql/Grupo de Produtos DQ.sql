
SELECT BM_DESC, BM_GRUPO,
	CASE 
		WHEN BM_GRUPO BETWEEN '1000' AND '1999' THEN 'PIGMENTOS'
		WHEN BM_GRUPO BETWEEN '2000' AND '2999' THEN 'ÓXIDOS'
		WHEN BM_GRUPO BETWEEN '3000' AND '3999' THEN 'PÓS-METALICOS'
		WHEN BM_GRUPO BETWEEN '4000' AND '4999' THEN 'DISPERSĂO'
		WHEN BM_GRUPO BETWEEN '5000' AND '5999' THEN 'REVENDA'
		ELSE 'OUTROS'
	END AS BM_MCCATEG
FROM SBM010
WHERE SBM010.D_E_L_E_T_ = ''
